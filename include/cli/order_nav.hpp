#pragma once
/**
 * @file include/cli/order_nav.hpp
 * @brief Ordered navigation helpers (TOP/BOTTOM/SKIP) for INX/CNX/CDX orderstate.
 *
 * Key goal: avoid building full in-memory recno lists for navigation.
 *
 * INX support:
 * - Reads on-disk "1INX" (legacy variable-length) and "2INX" (fixed-length + pos table).
 * - TOP/BOTTOM are O(1) for 2INX; O(n) scan for 1INX bottom (cached).
 * - SKIP is O(1) for 2INX; O(n) streaming scan (two-pass) for 1INX.
 *
 * CNX/CDX support:
 * - CNX: stopgap builds in-memory ordered recno list by evaluating tag on current table.
 * - CDX: TEMPORARILY uses the same DB-derived ordered recno list as CNX.
 *
 * Rebuild policy:
 * - Indexes generated by REINDEX (INX), REBUILD (CNX), BUILDLMDB (CDX). Navigation only reads.
 *
 * Format-detection improvement:
 * - Uses xindex::fmt::detect(...) instead of "not CNX means INX" style branching.
 * - This keeps navigation extensible for SIX/SNX later without accidental fallthrough.
 */
