static inline bool order_first_recno(xbase::DbArea& area, std::int64_t& out_recno) {
    out_recno = 0;
    if (!area.isOpen()) return false;
    if (!orderstate::hasOrder(area)) return false;
    const std::string path = orderstate::orderName(area);

    switch (order_nav_detail::detect_fmt(path)) {
    case xindex::fmt::IndexFormat::CDX: {
        // CDX is the LMDB front end (64-bit DBF flavors). Stream the backend
        // cursor (IndexManager resolves the LMDB env via SET PATH LMDB) and stop
        // at the first record in display order. One index op, independent of
        // table size. Only fall back to the DB-scan cache if the cursor is
        // unavailable.
        const std::string tagName = orderstate::activeTag(area);
        if (tagName.empty()) return false;

        std::int64_t rn = 0;
        cli::order_stream_display(area, /*reverse=*/false,
            [&](std::uint64_t recno) -> bool {
                rn = static_cast<std::int64_t>(recno);
                return false; // stop at the first record
            });
        if (rn >= 1 && rn <= static_cast<std::int64_t>(area.recCount64())) { out_recno = rn; return true; }

        const bool asc = orderstate::isAscending(area);
        auto& cache = order_nav_detail::cnx_cache_singleton();
        if (!order_nav_detail::cnx_cache_build(cache, area, tagName)) return false;
        if (cache.recnos.empty()) return false;
        out_recno = asc ? static_cast<std::int64_t>(cache.recnos.front())
                        : static_cast<std::int64_t>(cache.recnos.back());
        return (out_recno != 0);
    }
    case xindex::fmt::IndexFormat::CNX: {
        const std::string tagName = orderstate::activeTag(area);
        if (tagName.empty()) return false;
        auto& cache = order_nav_detail::cnx_cache_singleton();
        if (!order_nav_detail::cnx_cache_build(cache, area, tagName)) return false;
        if (cache.recnos.empty()) return false;
        const bool asc = orderstate::isAscending(area);
        out_recno = asc ? static_cast<std::int64_t>(cache.recnos.front())
                        : static_cast<std::int64_t>(cache.recnos.back());
        return (out_recno != 0);
    }
    case xindex::fmt::IndexFormat::INX: {
        order_nav_detail::InxMeta meta{};
        const order_nav_detail::fs::path p(path);
        if (!order_nav_detail::read_meta(p, meta)) return false;
        int32_t first{}, last{};
        if (!order_nav_detail::read_first_last(p, meta, first, last)) return false;
        const bool asc = orderstate::isAscending(area);
        out_recno = asc ? first : last;
        return (out_recno != 0);
    }
    case xindex::fmt::IndexFormat::SIX:
        // SIX is recognized but not implemented.
        return false;
    case xindex::fmt::IndexFormat::SCX:
        // SNX placeholder/stub; see note in order_skip().
        return false;
    case xindex::fmt::IndexFormat::Unknown:
    default:
        return false;
    }
}

static inline bool order_last_recno(xbase::DbArea& area, std::int64_t& out_recno) {
    out_recno = 0;
    if (!area.isOpen()) return false;
    if (!orderstate::hasOrder(area)) return false;
    const std::string path = orderstate::orderName(area);

    switch (order_nav_detail::detect_fmt(path)) {
    case xindex::fmt::IndexFormat::CDX: {
        // CDX is the LMDB front end (64-bit DBF flavors). Stream the backend
        // cursor in reverse display order and stop at the first record, i.e. the
        // last record in display order. One index op, independent of table size.
        const std::string tagName = orderstate::activeTag(area);
        if (tagName.empty()) return false;

        std::int64_t rn = 0;
        cli::order_stream_display(area, /*reverse=*/true,
            [&](std::uint64_t recno) -> bool {
                rn = static_cast<std::int64_t>(recno);
                return false; // stop at the last record
            });
        if (rn >= 1 && rn <= static_cast<std::int64_t>(area.recCount64())) { out_recno = rn; return true; }

        const bool asc = orderstate::isAscending(area);
        auto& cache = order_nav_detail::cnx_cache_singleton();
        if (!order_nav_detail::cnx_cache_build(cache, area, tagName)) return false;
        if (cache.recnos.empty()) return false;
        out_recno = asc ? static_cast<std::int64_t>(cache.recnos.back())
                        : static_cast<std::int64_t>(cache.recnos.front());
        return (out_recno != 0);
    }
    case xindex::fmt::IndexFormat::CNX: {
        const std::string tagName = orderstate::activeTag(area);
        if (tagName.empty()) return false;
        auto& cache = order_nav_detail::cnx_cache_singleton();
        if (!order_nav_detail::cnx_cache_build(cache, area, tagName)) return false;
        if (cache.recnos.empty()) return false;
        const bool asc = orderstate::isAscending(area);
        out_recno = asc ? static_cast<std::int64_t>(cache.recnos.back())
                        : static_cast<std::int64_t>(cache.recnos.front());
        return (out_recno != 0);
    }
    case xindex::fmt::IndexFormat::INX: {
        order_nav_detail::InxMeta meta{};
        const order_nav_detail::fs::path p(path);
        if (!order_nav_detail::read_meta(p, meta)) return false;
        int32_t first{}, last{};
        if (!order_nav_detail::read_first_last(p, meta, first, last)) return false;
        const bool asc = orderstate::isAscending(area);
        out_recno = asc ? last : first;
        return (out_recno != 0);
    }
    case xindex::fmt::IndexFormat::SIX:
        // SIX is recognized but not implemented.
        return false;
    case xindex::fmt::IndexFormat::SCX:
        // SNX placeholder/stub; see note in order_skip().
        return false;
    case xindex::fmt::IndexFormat::Unknown:
    default:
        return false;
    }
}

static inline bool order_top(xbase::DbArea& area) {
    if (!area.isOpen()) return false;
    std::int64_t rn{};
    if (order_first_recno(area, rn) && rn >= 1 && rn <= static_cast<std::int64_t>(area.recCount64())) {
        if (!area.gotoRec64(static_cast<std::uint64_t>(rn))) return false;
        return area.readCurrent();
    }
    if (!area.top()) return false;
    return area.readCurrent();
}

static inline bool order_bottom(xbase::DbArea& area) {
    if (!area.isOpen()) return false;
    std::int64_t rn{};
    if (order_last_recno(area, rn) && rn >= 1 && rn <= static_cast<std::int64_t>(area.recCount64())) {
        if (!area.gotoRec64(static_cast<std::uint64_t>(rn))) return false;
        return area.readCurrent();
    }
    if (!area.bottom()) return false;
    return area.readCurrent();
}

static inline bool order_skip(xbase::DbArea& area, int delta) {
    if (!area.isOpen()) return false;
    if (delta == 0) return area.readCurrent();

    if (!orderstate::hasOrder(area)) {
        if (!area.skip(delta)) return false;
        return area.readCurrent();
    }

    const std::string path = orderstate::orderName(area);

    switch (order_nav_detail::detect_fmt(path)) {
    case xindex::fmt::IndexFormat::CDX: {
        // CDX is the LMDB front end.
        const std::string tag = orderstate::activeTag(area);
        if (tag.empty()) return false;

        const bool asc = orderstate::isAscending(area);
        const int delta_eff = asc ? delta : -delta;

        // Fast path: a single ordered cursor step from the current record --
        // seek the current (key,recno) and step in the LMDB index. No position
        // map, no DBF scan; O(log n). This is what makes the FIRST ordered SKIP
        // instant.
        {
            std::int64_t rn = 0;
            const cli::OrderStep st = cli::order_step_cdx(area, delta_eff, rn);
            if (st == cli::OrderStep::Moved) {
                if (rn < 1 || rn > static_cast<std::int64_t>(area.recCount64())) return false;
                if (!area.gotoRec64(static_cast<std::uint64_t>(rn))) return false;
                return area.readCurrent();
            }
            // TODO(RECNO64 M5): the position-map fallback cache below is still 32-bit
            // (build_cdx_recnos_from_lmdb / CnxCache.recnos / uint32 rn); widen with O2.
            if (st == cli::OrderStep::Boundary) {
                return false; // at an order endpoint; no movement (do NOT fall back)
            }
            // Unavailable -> fall through to the position-map cache.
        }

        // Fallback: position-map cache built from the ascending index cursor
        // (first use materializes once), then the DB-scan cache if even that is
        // unavailable.
        auto& cache = order_nav_detail::cnx_cache_singleton();
        bool built = order_nav_detail::cdx_cache_build(cache, area, tag, path);
        if (!built) {
            if (!order_nav_detail::cnx_cache_build(cache, area, tag)) return false;
        }
        if (cache.recnos.empty()) return false;

        const std::int64_t cur = static_cast<std::int64_t>(area.recno64());
        if (cur < 1 || cur > static_cast<std::int64_t>(area.recCount64())) return false;

        auto it = cache.pos.find(static_cast<std::uint64_t>(cur));
        if (it == cache.pos.end()) return false;

        // Partial-to-boundary (matches the cursor step and xBase SKIP-to-EOF).
        std::int64_t next = it->second + delta_eff;
        const std::int64_t last = static_cast<std::int64_t>(cache.recnos.size()) - 1;
        if (next < 0) next = 0;
        if (next > last) next = last;
        if (next == it->second) return false; // already at the boundary; no move

        const std::int64_t rn = static_cast<std::int64_t>(cache.recnos[static_cast<size_t>(next)]);
        if (rn < 1 || rn > static_cast<std::int64_t>(area.recCount64())) return false;
        if (!area.gotoRec64(static_cast<std::uint64_t>(rn))) return false;
        return area.readCurrent();
    }

    case xindex::fmt::IndexFormat::CNX: {
        const std::string tag = orderstate::activeTag(area);
        if (tag.empty()) return false;

        auto& cache = order_nav_detail::cnx_cache_singleton();
        if (!order_nav_detail::cnx_cache_build(cache, area, tag) || cache.recnos.empty()) return false;

        const bool asc = orderstate::isAscending(area);
        const int delta_eff = asc ? delta : -delta;

        const std::int64_t cur = static_cast<std::int64_t>(area.recno64());
        if (cur < 1 || cur > static_cast<std::int64_t>(area.recCount64())) return false;

        auto it = cache.pos.find(static_cast<std::uint64_t>(cur));
        if (it == cache.pos.end()) return false;

        // Partial-to-boundary (matches the cursor step and xBase SKIP-to-EOF).
        std::int64_t next = it->second + delta_eff;
        const std::int64_t last = static_cast<std::int64_t>(cache.recnos.size()) - 1;
        if (next < 0) next = 0;
        if (next > last) next = last;
        if (next == it->second) return false; // already at the boundary; no move

        const std::int64_t rn = static_cast<std::int64_t>(cache.recnos[static_cast<size_t>(next)]);
        if (rn < 1 || rn > static_cast<std::int64_t>(area.recCount64())) return false;
        if (!area.gotoRec64(static_cast<std::uint64_t>(rn))) return false;
        return area.readCurrent();
    }

    case xindex::fmt::IndexFormat::INX: {
        std::vector<uint32_t> recnos;
        if (!order_nav_detail::load_inx_recnos(path, area.recCount(), recnos) || recnos.empty()) {
            if (!area.skip(delta)) return false;
            return area.readCurrent();
        }

        const bool asc = orderstate::isAscending(area);
        const int delta_eff = asc ? delta : -delta;

        const int32_t cur = area.recno();
        if (cur < 1 || cur > area.recCount()) return false;

        int32_t pos = -1;
        for (int32_t i = 0; i < static_cast<int32_t>(recnos.size()); ++i) {
            if (recnos[static_cast<size_t>(i)] == static_cast<uint32_t>(cur)) {
                pos = i;
                break;
            }
        }
        if (pos < 0) return false;

        // Partial-to-boundary (matches xBase SKIP-to-EOF).
        int32_t next = pos + delta_eff;
        const int32_t last = static_cast<int32_t>(recnos.size()) - 1;
        if (next < 0) next = 0;
        if (next > last) next = last;
        if (next == pos) return false; // already at the boundary; no move

        const int32_t rn = static_cast<int32_t>(recnos[static_cast<size_t>(next)]);
        if (rn < 1 || rn > area.recCount()) return false;
        if (!area.gotoRec(rn)) return false;
        return area.readCurrent();
    }

    case xindex::fmt::IndexFormat::SIX:
        // SIX is registered as a future/student index format.
        // Do not silently fall back to physical order while an unsupported order is active.
        return false;

    case xindex::fmt::IndexFormat::SCX:
        // SNX stub: this project currently names the second student/custom slot SCX
        // in IndexFormat. Treat it as the SNX placeholder until the enum is renamed
        // or an explicit SNX value is added in xindex/index_format_detect.hpp.
        return false;

    case xindex::fmt::IndexFormat::Unknown:
    default:
        if (!area.skip(delta)) return false;
        return area.readCurrent();
    }
}

static inline void order_nav_invalidate(xbase::DbArea& area) {
    if (!area.isOpen()) return;
    if (!orderstate::hasOrder(area)) return;

    const std::string path = orderstate::orderName(area);
    const auto fmt = order_nav_detail::detect_fmt(path);

    if (fmt == xindex::fmt::IndexFormat::INX) {
        order_nav_detail::cache_invalidate_path(order_nav_detail::fs::path(path));
    }

    if (fmt == xindex::fmt::IndexFormat::CNX ||
        fmt == xindex::fmt::IndexFormat::CDX) {
        order_nav_detail::cnx_cache_invalidate();
    }
}

