struct CnxCache {
    xbase::DbArea* area{nullptr};
    std::string tag;
    std::int64_t recCount{0};                              // RECNO64 O2
    std::vector<std::uint64_t> recnos;                     // RECNO64 O2: 64-bit ordered recnos
    std::unordered_map<std::uint64_t, std::int64_t> pos;   // recno -> ordinal position
};

static inline CnxCache& cnx_cache_singleton() {
    static CnxCache c;
    return c;
}

static inline void cnx_cache_invalidate() {
    CnxCache& c = cnx_cache_singleton();
    c.area = nullptr;
    c.tag.clear();
    c.recCount = 0;
    c.recnos.clear();
    c.pos.clear();
}

static inline bool cnx_cache_build(CnxCache& cache, xbase::DbArea& area, const std::string& tagName)
{
    if (!area.isOpen()) return false;
    const std::int64_t N = static_cast<std::int64_t>(area.recCount64());
    if (cache.area == &area &&
        cache.tag == tagName &&
        cache.recCount == N &&
        !cache.recnos.empty())
    {
        return true;
    }
    cache.area = &area;
    cache.tag = tagName;
    cache.recCount = N;
    cache.recnos.clear();
    cache.pos.clear();

    // build_cnx_recnos_from_db stays 32-bit (CNX is a V32 format, and the builder is
    // shared with db_tuple_stream). Widen its recnos up into the 64-bit cache here.
    std::vector<uint32_t> tmp32;
    if (!build_cnx_recnos_from_db(area, tagName, tmp32))
        return false;
    cache.recnos.assign(tmp32.begin(), tmp32.end());

    cache.pos.reserve(cache.recnos.size());
    for (std::int64_t i = 0; i < static_cast<std::int64_t>(cache.recnos.size()); ++i) {
        cache.pos[cache.recnos[static_cast<size_t>(i)]] = i;
    }
    return !cache.recnos.empty();
}

// CDX ordered cache built directly from the LMDB index (already key-sorted), so
// there is no DBF scan and no key sort. This is the correct source for CDX SKIP;
// the CNX DB-scan builder (cnx_cache_build) is reserved for 32-bit CNX flavors.
// Returns false if LMDB is unavailable so callers can fall back safely.
static inline bool cdx_cache_build(CnxCache& cache,
                                   xbase::DbArea& area,
                                   const std::string& tagName,
                                   const std::string& cdxPath)
{
    if (!area.isOpen()) return false;
    const std::int64_t N = static_cast<std::int64_t>(area.recCount64());
    if (cache.area == &area &&
        cache.tag == tagName &&
        cache.recCount == N &&
        !cache.recnos.empty())
    {
        return true;
    }
    cache.area = &area;
    cache.tag = tagName;
    cache.recCount = N;
    cache.recnos.clear();
    cache.pos.clear();
    (void)cdxPath;

    // Ascending index order via the IndexManager backend cursor. This resolves
    // the LMDB env correctly (unlike a raw <cdx>.d guess) and avoids scanning +
    // sorting the DBF. order_collect_recnos_asc always yields ascending index
    // order, which is what the position/step math below expects.
    std::vector<std::uint64_t> tmp;
    if (!cli::order_collect_recnos_asc(area, tmp, nullptr, nullptr) || tmp.empty())
        return false;

    cache.recnos.reserve(tmp.size());
    for (std::uint64_t rn : tmp) {
        if (rn >= 1 && rn <= static_cast<std::uint64_t>(N))
            cache.recnos.push_back(rn);
    }

    cache.pos.reserve(cache.recnos.size());
    for (std::int64_t i = 0; i < static_cast<std::int64_t>(cache.recnos.size()); ++i) {
        cache.pos[cache.recnos[static_cast<size_t>(i)]] = i;
    }
    return !cache.recnos.empty();
}

